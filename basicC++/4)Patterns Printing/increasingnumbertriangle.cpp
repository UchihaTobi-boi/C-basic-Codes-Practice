#include <iostream>
using namespace std;
int main(){
        int a = 4;
        int n = 1;

        for (int i = 0; i <= a; i++)
        {
            for (int x = 1; x <= i; x++)
            {
                cout<<n<<" ";
                n++;
            }
            cout<<endl;

        }
        



}
